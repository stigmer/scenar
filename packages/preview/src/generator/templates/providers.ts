import type { ScanResult } from "../../scanner/types.js";

/**
 * Render `providers.tsx` — the preview provider chain.
 * Created once by `scenar preview init`, never overwritten by sync.
 */
export function renderProviders(scanResult: ScanResult): string {
  const providers = scanResult.detectedProviders;

  if (providers.length === 0) {
    return EMPTY_PROVIDERS;
  }

  const providerChain = buildProviderChain(providers);
  return providerChain;
}

function buildProviderChain(providers: readonly string[]): string {
  const filtered = providers.filter(
    (p) => !isAuthProvider(p) && !isRouterProvider(p),
  );

  const lines: string[] = [
    `// Generated by scenar preview init — customize freely.`,
    `// This file will NOT be overwritten by \`scenar preview sync\`.`,
    `// To re-detect providers: scenar preview init --reset-providers`,
    ``,
    `import type { ReactNode } from "react";`,
  ];

  if (hasRouter(providers)) {
    lines.push(`// Router replaced with MemoryRouter for preview isolation.`);
    lines.push(`// import { MemoryRouter } from "react-router-dom";`);
  }

  lines.push(``);
  lines.push(`interface PreviewProvidersProps {`);
  lines.push(`  readonly children: ReactNode;`);
  lines.push(`}`);
  lines.push(``);

  if (filtered.length === 0) {
    lines.push(`export function PreviewProviders({ children }: PreviewProvidersProps) {`);
    lines.push(`  return <>{children}</>;`);
    lines.push(`}`);
  } else {
    lines.push(`/**`);
    lines.push(` * Provider chain detected from your app entry point.`);
    lines.push(` * Customize this to match your preview requirements.`);
    lines.push(` *`);
    lines.push(` * Detected providers: ${providers.join(", ")}`);
    lines.push(` * Auth providers removed (not needed for demos).`);
    lines.push(` */`);
    lines.push(`export function PreviewProviders({ children }: PreviewProvidersProps) {`);
    lines.push(`  // TODO: Import and configure the providers below.`);
    lines.push(`  // The following providers were detected in your app:`);
    for (const p of filtered) {
      lines.push(`  //   - ${p}`);
    }
    lines.push(`  return <>{children}</>;`);
    lines.push(`}`);
  }

  lines.push(``);

  return lines.join("\n");
}

function isAuthProvider(name: string): boolean {
  const lower = name.toLowerCase();
  return (
    lower.includes("auth") ||
    lower.includes("clerk") ||
    lower.includes("session") ||
    lower.includes("supabase")
  );
}

function isRouterProvider(name: string): boolean {
  const lower = name.toLowerCase();
  return lower.includes("router") || lower === "browserrouter";
}

function hasRouter(providers: readonly string[]): boolean {
  return providers.some((p) => isRouterProvider(p));
}

const EMPTY_PROVIDERS = `// Generated by scenar preview init — customize freely.
// This file will NOT be overwritten by \`scenar preview sync\`.
// To re-detect providers: scenar preview init --reset-providers

import type { ReactNode } from "react";

interface PreviewProvidersProps {
  readonly children: ReactNode;
}

/**
 * Provider wrapper for preview components.
 * Add your app's providers here (theme, query client, etc.).
 */
export function PreviewProviders({ children }: PreviewProvidersProps) {
  return <>{children}</>;
}
`;
