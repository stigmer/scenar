import * as path from "path";
import type { ScanResult } from "../../scanner/types.js";

/**
 * Render `views.generated.ts` — the auto-discovered component registry.
 * This file is always overwritten by `scenar preview sync`.
 */
export function renderViewsGenerated(
  scanResult: ScanResult,
  outputDir: string,
): string {
  const components = scanResult.discovered;
  if (components.length === 0) {
    return [
      HEADER,
      "",
      "export const generatedViews = {} as const;",
      "",
    ].join("\n");
  }

  const imports: string[] = [];
  const entries: string[] = [];

  for (const comp of components) {
    const relPath = buildRelativeImport(comp.filePath, outputDir);
    if (comp.exportType === "default") {
      imports.push(`import ${comp.name} from "${relPath}";`);
    } else {
      imports.push(`import { ${comp.name} } from "${relPath}";`);
    }
    entries.push(`  ${comp.name},`);
  }

  return [
    HEADER,
    "",
    ...imports,
    "",
    "export const generatedViews = {",
    ...entries,
    "} as const;",
    "",
  ].join("\n");
}

function buildRelativeImport(
  absoluteFilePath: string,
  outputDir: string,
): string {
  let rel = path.relative(outputDir, absoluteFilePath);
  rel = rel.replace(/\.(tsx?|jsx?)$/, "");
  if (!rel.startsWith(".")) rel = "./" + rel;
  return rel;
}

const HEADER = `// AUTO-GENERATED by scenar preview — do not edit.
// Re-run \`scenar preview sync\` to update.
// To add components manually, use views.custom.tsx instead.`;
